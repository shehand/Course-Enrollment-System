    private void btnSubmitLecturerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitLecturerActionPerformed
        Lecturers lec = new Lecturers();
        
        lec.setLecID(txtLecID.getText());
        lec.setEmail(txtEmailLec.getText());
        lec.setName(txtLecName.getText());
        lec.setPassword(txtLecPass.getText());
        lec.setPhoneNumber(Integer.parseInt(txtPhoneNumberLec.getText()));
        lec.setSubject(txtSubCodeLec.getText());
        lec.setPosition(cmboPosition.getSelectedItem().toString());
        lec.setFaculty(cmboFacNameLec.getSelectedItem().toString());
        
        LecturerDBOperations ops = new LecturerDBOperations();
        if(ops.addLecturer(lec)){
            JOptionPane.showMessageDialog(this, "Inserted Successfully");
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Oops! Can't add this data this moment");
        }
    }//GEN-LAST:event_btnSubmitLecturerActionPerformed

